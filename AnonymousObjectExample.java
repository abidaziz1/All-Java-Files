public class AnonymousObjectExample {
    void display() {
        System.out.println("Anonymous Object Example");
    }

    public static void main(String[] args) {
        new AnonymousObjectExample().display();  // Output: Anonymous Object Example
    }
}
/*
 * An anonymous object is an object without a reference variable. It is used only once and is generally used for instantiating classes and invoking methods on the fly.
 */
